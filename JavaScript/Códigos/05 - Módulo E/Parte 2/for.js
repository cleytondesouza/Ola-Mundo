//Estrutura de repetição for (PARA)
/*
Essa estrutura tem 3 passos
1 - Inicio - criando a variavel e atribuido valor
2 - Teste lógico
3 - Incremento

Separe essas partes com ponto e vírgula, veja o exemplo abaixo
*/

console.log('Vai começar')

